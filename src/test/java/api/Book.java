package api;

public class Book{
	private Object isElectronicBook;
	private Object year;
	private Object author;
	private Object name;
	private Object id;

	public Object isIsElectronicBook(){
		return isElectronicBook;
	}

	public Object getIsElectronicBook() {
		return isElectronicBook;
	}

	public Object getYear() {
		return year;
	}

	public Object getAuthor() {
		return author;
	}

	public Object getName() {
		return name;
	}

	public Object getId() {
		return id;
	}

}
